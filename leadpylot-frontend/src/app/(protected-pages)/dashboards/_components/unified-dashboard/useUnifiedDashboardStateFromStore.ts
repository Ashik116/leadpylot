      setIsSendToOutDialogOpen: s.setIsSendToOutDialogOpen,
      isPdfModalOpen: s.isPdfModalOpen,
      setIsPdfModalOpen: s.setIsPdfModalOpen,
      selectedRowForPdf: s.selectedRowForPdf,
      setSelectedRowForPdf: s.setSelectedRowForPdf,
      isOpeningDetailsOpen: s.isOpeningDetailsOpen,
      setIsOpeningDetailsOpen: s.setIsOpeningDetailsOpen,
      selectedOpeningForDetails: s.selectedOpeningForDetails,
      setSelectedOpeningForDetails: s.setSelectedOpeningForDetails,
      isOfferDetailsOpen: s.isOfferDetailsOpen,
