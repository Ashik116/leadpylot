      setHasManuallyClearedGroupFilter: s.setHasManuallyClearedGroupFilter,
      isMultiLevelGroupingApplied: s.isMultiLevelGroupingApplied,
      setIsMultiLevelGroupingApplied: s.setIsMultiLevelGroupingApplied,
      hasTransferredOffer: s.hasTransferredOffer,
      setHasTransferredOffer: s.setHasTransferredOffer,
      isAssignTicketDialogOpen: s.isAssignTicketDialogOpen,
      setIsAssignTicketDialogOpen: s.setIsAssignTicketDialogOpen,
      selectedTicketForAssign: s.selectedTicketForAssign,
      setSelectedTicketForAssign: s.setSelectedTicketForAssign,
      isBulkDownloadConfirmOpen: s.isBulkDownloadConfirmOpen,
